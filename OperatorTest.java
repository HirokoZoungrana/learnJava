class OperatorTest {

	public static void main (String[] args) {
	
		int age = 31;

		System.out.println(age > 30);
		
		System.out.println(age == 35);
		
		System.out.println(3 != 3);

		System.out.println(age != 45);
		
		System.out.println(!(age == 35));
		//age != 35と同じ意味

	}
}